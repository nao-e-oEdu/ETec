CREATE DATABASE BD_Colegio;
USE BD_Colegio;

CREATE TABLE disciplinas (
	CodDisciplina CHAR(2) PRIMARY KEY,
	NomeDisciplina VARCHAR(30)
);

CREATE TABLE cursos (
	codcurso CHAR(2) PRIMARY KEY,
	nome VARCHAR(50),
	coddisc1 CHAR(2) FOREIGN KEY REFERENCES disciplinas(CodDisciplina),
	coddisc2 CHAR(2) FOREIGN KEY REFERENCES disciplinas(CodDisciplina),
	coddisc3 CHAR(2) FOREIGN KEY REFERENCES disciplinas(CodDisciplina)
);

CREATE TABLE alunos (
	matricula VARCHAR(5) PRIMARY KEY,
	nome VARCHAR(50),
	endereco VARCHAR(50),
	cidade VARCHAR(30),
	codcurso CHAR(2) FOREIGN KEY REFERENCES cursos(codcurso)
);

